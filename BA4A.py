def ProteinTranslation(RNA,geneticCode):
    peptide=""
    for i in range(0,len(RNA),3):
        tmp=RNA[i:(i+3)]
        for key,value in geneticCode.items():
            if tmp==key:
                peptide+=geneticCode[key]
    return peptide[:-1]


